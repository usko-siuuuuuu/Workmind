import smtplib
from email.mime.text import MIMEText
from email.mime.multipart import MIMEMultipart
import time

def send(smtp_port, from_email, to_emails, cc_emails, subject, body, reply_to_msg_id=None):
    msg = MIMEMultipart()
    msg["From"] = from_email
    msg["To"] = ", ".join(to_emails)
    if cc_emails:
        msg["Cc"] = ", ".join(cc_emails)
    msg["Subject"] = subject
    if reply_to_msg_id:
        msg["In-Reply-To"] = reply_to_msg_id
        msg["References"] = reply_to_msg_id
    msg.attach(MIMEText(body, "plain", "utf-8"))
    
    all_recipients = to_emails + cc_emails
    
    # Отправляем на основной сервер
    with smtplib.SMTP("localhost", smtp_port) as server:
        server.sendmail(from_email, all_recipients, msg.as_string())
    
    # Если среди получателей есть наши сотрудники — дублируем на наш сервер
    our_domain = "monotekstroy.ru"
    our_recipients = [r for r in all_recipients if our_domain in r]
    if our_recipients and smtp_port != MONOTEK:
        with smtplib.SMTP("localhost", MONOTEK) as server:
            server.sendmail(from_email, our_recipients, msg.as_string())
        print(f"  → продублировано на МонотекСтрой: {our_recipients}")
    
    print(f"✓ [{smtp_port}] {from_email} → {', '.join(all_recipients)}: {subject}")
    time.sleep(1)
    return msg["Message-ID"]

