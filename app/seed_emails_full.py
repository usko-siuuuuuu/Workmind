def send(smtp_port, from_email, to_emails, cc_emails, subject, body, attachments=None):
    msg = MIMEMultipart()
    msg["From"] = from_email
    msg["To"] = ", ".join(to_emails)
    if cc_emails:
        msg["Cc"] = ", ".join(cc_emails)
    msg["Subject"] = subject
    msg.attach(MIMEText(body, "plain", "utf-8"))

    if attachments:
        for filename in attachments:
            filepath = os.path.join(ATTACHMENTS_DIR, filename)
            if os.path.exists(filepath):
                with open(filepath, "rb") as f:
                    part = MIMEBase("application", "octet-stream")
                    part.set_payload(f.read())
                encoders.encode_base64(part)
                part.add_header("Content-Disposition", f"attachment; filename={filename}")
                msg.attach(part)

    all_recipients = to_emails + cc_emails
    with smtplib.SMTP("localhost", smtp_port) as server:
        server.sendmail(from_email, all_recipients, msg.as_string())

    our_domain = "monotekstroy.ru"
    our_recipients = [r for r in all_recipients if our_domain in r]
    if our_recipients and smtp_port != MONOTEK:
        with smtplib.SMTP("localhost", MONOTEK) as server:
            server.sendmail(from_email, our_recipients, msg.as_string())

    att_str = f" [{', '.join(attachments)}]" if attachments else ""
    print(f"✓ [{smtp_port}] {from_email} → {', '.join(all_recipients)}: {subject}{att_str}")
    time.sleep(0.5)

